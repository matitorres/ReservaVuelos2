    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
      CompraData gestorCompra = new CompraData();
      Compra nuevaCompra= new Compra();
      Asiento asiento= new Asiento();
      AsientoData aD= new AsientoData();
      Cliente cliente=new Cliente();
      ClienteData cD=new ClienteData();
      cliente =cD.buscarClientePorDni(Integer.parseInt(dni.getText()));
      asiento=aD.buscarAsiento(Integer.parseInt(idAsientoComprado.getText()));
      nuevaCompra.setAsiento(asiento);
      asiento.setDisponible(false);
      aD.actualizarAsiento(asiento);
      nuevaCompra.setCliente(cliente);
      Timestamp horaActual = new Timestamp(System.currentTimeMillis());
      nuevaCompra.setFechaCompra(horaActual);
      nuevaCompra.setNroTarjeta(Integer.parseInt(tarjeta.getText()));
      try {
            int exito=gestorCompra.guardarCompra(nuevaCompra);
            if(exito==1){
            panelDatosCliente.setVisible(false);
            jPanelDatosPasaje.setVisible(false);
            panelContenedorAsientos.setVisible(false);
            jButtonDisponible.setVisible(false);
            jButtonOcupado.setVisible(false);
            JOptionPane.showMessageDialog(null,"Su Compra se realizado Exitosamente");
            vaciarCajasCliente();
            vaciarCajasDatosVuelo();
            panel.removeAll();
            panel1.removeAll();
            }else{
            }
        } catch (SQLException ex) {
            Logger.getLogger(VistaPrincipal.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

